    private void jButtonCambianteIgualMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonCambianteIgualMouseClicked
        if (!Pattern.matches("^-?[0-9]+[+-]+[0-9]+", jTextFieldDatos.getText())) {
            evt.consume();
            JOptionPane.showMessageDialog(this,"Se procede a guardar en la lista de operaciones","Expresi√≥n correcta",JOptionPane.ERROR_MESSAGE);
            Date fecha = new Date();
            String hora = fecha.getHours() + ":" + fecha.getMinutes() + ":" + fecha.getSeconds();
            String expresion = jTextFieldDatos.getText();
            DatoValidado dato = new DatoValidado(fecha, hora, expresion);
            Logica.addDato(dato);
        } else {
            JOptionPane.showMessageDialog(this,"Expresi√≥n incorrecta","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonCambianteIgualMouseClicked

